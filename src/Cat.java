public class Cat {


    string catName = "";
    string catGender = "";
    int catAge;


    public void newCat(string newCatName, int newCatAge, string newCatGender) {
        catName = newCatName;
        catAge = newCatAge;
    }

    public getCatAge{

        return catAge;
    }

    public getCatGender{

        return catGender;
    }
}

