public class Dog {


    String dogName = "";
    String dogGender = "";
    int dogAge;


    public void newDog(string newDogName, int newDogAge, string newDogGender) {
        dogName = newDogName;
        dogAge = newDogAge;
    }

    public getDogAge{

        return dogAge;
    }

    public getDogGender{

        return dogGender;
    }
}